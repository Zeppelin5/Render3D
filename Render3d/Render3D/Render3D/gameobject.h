#pragma once
#include "transform.h"

class GameObject
{
public:
	Transform *transform;

	GameObject()
	{
		transform = new Transform;
	}

	virtual~GameObject()
	{
		delete transform;
		transform = nullptr;
	}
private:

};


class Model :public GameObject
{
public:

	Model()
	{
		mesh = new Mesh;
	}

	virtual Mesh* GetMesh() { return nullptr; }

	virtual~Model()
	{
		delete mesh;
		mesh = nullptr;
	}

protected:
	Mesh *mesh;
};



